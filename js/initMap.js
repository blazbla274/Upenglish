    var map = new google.maps.Map(document.getElementById("map"), {
        zoom: 12,
        center: location,
        mapTypeControlOptions: {
            mapTypeIds: ['roadmap', 'satellite', 'hybrid', 'terrain', 'night_map']
        }
    });
    var marker = new google.maps.Marker({
        position: location,
        map: map
    });

